package homework12172022;

public class EvenOrOddProgram {
    public static void main(String[] args) {

        double number=-30.2;

        double reminder=number%2;
        System.out.println(reminder);
        if (reminder==0){
            System.out.println("Even number");
        }else {
            System.out.println("Odd number");
        }




    }
}
